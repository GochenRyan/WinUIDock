    /// </summary>
    Left = 0,

    /// <summary>
    /// Bottom alignment.
    /// </summary>
    Bottom = 1,

    /// <summary>
    /// Right alignment.
    /// </summary>
    Right = 2,

    /// <summary>
    /// Top alignment.
    /// </summary>
    Top = 3
}